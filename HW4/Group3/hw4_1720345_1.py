#1. 编写程序，计算百钱买百鸡问题。假设公鸡5元一只，母鸡3元一只，小鸡1元三只，现在有100块钱，想买100只鸡，问有多少种买法？

f = 0
for i in range(0, 20):
    for j in range(0, 33):
        k = 100 - i - j
        if (k % 3 == 0) and (5 * i + 3 * j + k / 3 == 100):
            print('公鸡：%s 母鸡：%s 小鸡：%s' % (i, j, k))
            f = f + 1
print('共有%d种方法' % f)